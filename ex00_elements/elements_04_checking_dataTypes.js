/*
How to check what type of data types are we working with.
*/

/*
Recommend doing this in the browser as it shows the type faster.
*/

// It's as easy as writing 'typeof'.
typeof 12;
console.log(typeof 12);

// In this sense it's a cool use, but it's particularly usefull once we start working with libraries.
var age = null;
console.log(typeof age);