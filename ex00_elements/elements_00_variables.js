/*
Variables can be defined by the use of the keyword "var"
*/

var my_name = "My name is Imanol";

console.log(my_name);

/*
Variables in JS can also be declared "in bulk"
*/

var first_name, last_name, email;
first_name = 'Imanol';
last_name = "de la Iglesia";
email = "delaiglesia@gmail.com";

console.log(first_name);
console.log(last_name);
console.log(email);